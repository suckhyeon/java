package a.basic;

public class Output {

	public static void main(String[] args) {
		// 한줄을 출력하고 개행을 하는 출력문
		System.out.println("안녕하세요.");
		System.out.println("서재용입니다.");
		
		//단순하게 출력을 할 때 사용하는 출력문
		System.out.print("제 성별은\n\n\n\n");
		//print에서 개행을 하고싶을 때는 \n을 이용한다.
		System.out.print("남자입니다.");
	}

}
